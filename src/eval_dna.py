#!/usr/bin/env python3
"""
Evaluate LLM judges using the DnA-Eval methodology (Decomposition and Aggregation).

DnA-Eval decomposes evaluation into per-instruction aspects with weighted scoring.
For each instruction, the pipeline:
  1. Generates 3 evaluation aspect questions (1 LLM call)
  2. Generates importance weights for those aspects (1 LLM call)
  3. Scores each response on each aspect (3 LLM calls pairwise, 3 pointwise)
  4. Aggregates scores via weighted sum (in code, no LLM call)

Benchmarks:
    Pairwise:  AlpacaEval, MTBench, BiGGen Bench
    Pointwise: HelpSteer2, ProfBench, HealthBench

Aspects and weights are cached per instruction to avoid redundant calls when
the same instruction appears in multiple pairs.

Reference:
    Li et al. (2025). DnA-Eval: Enhancing Large Language Model Evaluation
    through Decomposition and Aggregation. COLING 2025.
    https://github.com/WING-NUS/Decompose-and-Aggregate

Authentication (environment variables):
    AWS_BEARER_TOKEN_BEDROCK  -- Bearer token for Bedrock API
    AWS_REGION                -- AWS region (default: us-east-1)

Usage:
    # Pairwise: AlpacaEval
    python src/eval_dna.py --benchmark alpacaeval --judges claude-sonnet-4

    # Pointwise: HelpSteer2
    python src/eval_dna.py --benchmark helpsteer2 --judges llama-3.1-8b

    # Quick test
    python src/eval_dna.py --benchmark helpsteer2 --judges llama-3.1-8b \\
        --max-examples 5
"""

