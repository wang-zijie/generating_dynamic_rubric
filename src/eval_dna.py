def _generate_aspects_vllm(vllm_judge, instruction, aspects_cache, num_aspects=3):
    """Generate aspects using vLLM (single call, with caching)."""
    if instruction in aspects_cache:
        return aspects_cache[instruction]
    user_msg = ASPECT_GEN_USER.format(num_aspects=num_aspects, instruction=instruction)
    raw = vllm_judge.generate(ASPECT_GEN_SYSTEM, user_msg)
    aspects = parse_aspects(raw, num_aspects)
    if not aspects:
        aspects = [line.strip() for line in raw.strip().split("\n") if line.strip()][:num_aspects]
    aspects_cache[instruction] = aspects
    return aspects


def _generate_weights_vllm(vllm_judge, instruction, aspects, weights_cache):
    """Generate weights using vLLM (single call, with caching)."""
    if instruction in weights_cache:
        return weights_cache[instruction]
    num_aspects = len(aspects)
    aspects_text = "\n".join(f"{i+1}. {a}" for i, a in enumerate(aspects))
    user_msg = WEIGHT_GEN_USER.format(
        num_aspects=num_aspects, instruction=instruction, aspects=aspects_text,
    )
    raw = vllm_judge.generate(WEIGHT_GEN_SYSTEM, user_msg)
    weights = parse_weights(raw, num_aspects)
    if weights is None:
        weights = [1.0 / num_aspects] * num_aspects
    weights_cache[instruction] = weights
    return weights


def run_pairwise_judge_vllm(
    vllm_judge,
    judge_name: str,
    pairs: list[dict],
    output_dir: Path,
    *,
    num_aspects: int = 3,
    max_examples: int | None = None,
    checkpoint_every: int = 10,
    batch_size: int = 32,
) -> tuple[Path, dict]:
    """Run DnA-Eval on pairwise benchmark pairs using vLLM."""
    if max_examples is not None:
        pairs = pairs[:max_examples]

    output_file = output_dir / f"judge_{judge_name}.json"
    aspects_file = output_dir / f"aspects_{judge_name}.json"
    weights_file = output_dir / f"weights_{judge_name}.json"

    results = load_checkpoint(output_file)
    aspects_cache = load_cache(aspects_file)
    weights_cache = load_cache(weights_file)
    start_idx = len(results)

    if start_idx >= len(pairs):
        logger.info("[%s] Already complete (%d results)", judge_name, start_idx)
        return output_file, compute_pairwise_metrics(results)

    logger.info(
        "[%s] Evaluating pairs %d -> %d (vLLM, batch_size=%d)",
        judge_name, start_idx, len(pairs), batch_size,
    )

    # Pre-generate aspects and weights for all unique instructions (batched)
    remaining_pairs = pairs[start_idx:]
    unique_instructions = list(dict.fromkeys(p["instruction"] for p in remaining_pairs))
    uncached_instructions = [inst for inst in unique_instructions if inst not in aspects_cache]

    if uncached_instructions:
        logger.info("Generating aspects for %d unique instructions...", len(uncached_instructions))
        aspect_prompts = [
            (ASPECT_GEN_SYSTEM, ASPECT_GEN_USER.format(num_aspects=num_aspects, instruction=inst))
            for inst in uncached_instructions
        ]
        aspect_outputs = vllm_judge.generate_batch(aspect_prompts, batch_size=batch_size)
        for inst, raw in zip(uncached_instructions, aspect_outputs):
            if raw:
                aspects = parse_aspects(raw, num_aspects)
                if not aspects:
                    aspects = [line.strip() for line in raw.strip().split("\n") if line.strip()][:num_aspects]
                aspects_cache[inst] = aspects
        save_cache(aspects_file, aspects_cache)

    uncached_weights = [inst for inst in unique_instructions if inst not in weights_cache and inst in aspects_cache]
    if uncached_weights:
        logger.info("Generating weights for %d unique instructions...", len(uncached_weights))
        weight_prompts = []
        for inst in uncached_weights:
            aspects = aspects_cache[inst]
            aspects_text = "\n".join(f"{i+1}. {a}" for i, a in enumerate(aspects))
            weight_prompts.append((
                WEIGHT_GEN_SYSTEM,
                WEIGHT_GEN_USER.format(num_aspects=len(aspects), instruction=inst, aspects=aspects_text),
            ))
        weight_outputs = vllm_judge.generate_batch(weight_prompts, batch_size=batch_size)
        for inst, raw in zip(uncached_weights, weight_outputs):
            if raw:
                weights = parse_weights(raw, len(aspects_cache.get(inst, [])))
                if weights is None:
                    n = len(aspects_cache.get(inst, []))
                    weights = [1.0 / n] * n if n > 0 else [1.0]
                weights_cache[inst] = weights
        save_cache(weights_file, weights_cache)

    # Now batch the per-aspect scoring calls
    for batch_start in tqdm(
        range(0, len(remaining_pairs), batch_size),
        desc=judge_name,
        total=(len(remaining_pairs) + batch_size - 1) // batch_size,
    ):
        batch = remaining_pairs[batch_start:batch_start + batch_size]

        # For each pair, we need num_aspects scoring calls
        scoring_prompts = []
        prompt_map = []  # (batch_idx, aspect_idx)
        for j, pair in enumerate(batch):
            instruction = pair["instruction"]
            aspects = aspects_cache.get(instruction, [])
            for a_idx, aspect in enumerate(aspects):
                user_msg = EVAL_PAIRWISE_USER.format(
                    instruction=instruction,
                    output_1=pair["output_1"],
                    output_2=pair["output_2"],
                    aspect=aspect,
                )
                scoring_prompts.append((EVAL_PAIRWISE_SYSTEM, user_msg))
                prompt_map.append((j, a_idx))

        scoring_outputs = vllm_judge.generate_batch(scoring_prompts, batch_size=batch_size * num_aspects)

        # Reconstruct results per pair
        pair_aspect_outputs = {j: [None] * num_aspects for j in range(len(batch))}
        for (j, a_idx), raw in zip(prompt_map, scoring_outputs):
            pair_aspect_outputs[j][a_idx] = raw or ""

        for j, pair in enumerate(batch):
            instruction = pair["instruction"]
            aspects = aspects_cache.get(instruction, [])
            weights = weights_cache.get(instruction, [1.0 / max(len(aspects), 1)] * max(len(aspects), 1))

            aspect_scores_1 = []
            aspect_scores_2 = []
            raw_aspect_outputs_list = pair_aspect_outputs[j]

            for raw in raw_aspect_outputs_list:
                s1, s2 = parse_pairwise_scores(raw)
                aspect_scores_1.append(s1)
                aspect_scores_2.append(s2)

            all_valid = all(s is not None for s in aspect_scores_1 + aspect_scores_2)
            if all_valid:
                ws1, ws2, preference = aggregate_pairwise(weights, aspect_scores_1, aspect_scores_2)
            else:
                ws1, ws2, preference = None, None, None

            result_entry = {
                "instruction": instruction,
                "aspects": aspects,
                "weights": weights,
                "aspect_scores_1": aspect_scores_1,
                "aspect_scores_2": aspect_scores_2,
                "raw_aspect_outputs": raw_aspect_outputs_list,
                "weighted_score_1": ws1,
                "weighted_score_2": ws2,
                "preference": preference,
                "human_majority": pair["human_majority"],
            }
            for key in ["generator", "human_preferences"]:
                if key in pair:
                    result_entry[key] = pair[key]
            results.append(result_entry)

        if len(results) % checkpoint_every == 0 or batch_start + batch_size >= len(remaining_pairs):
            save_checkpoint(output_file, results)

    save_cache(aspects_file, aspects_cache)
    save_cache(weights_file, weights_cache)
    metrics = compute_pairwise_metrics(results)
    logger.info("[%s] Done -- %d results -> %s", judge_name, len(results), output_file)
    return output_file, metrics


def run_pointwise_judge_vllm(
    vllm_judge,
    judge_name: str,
    items: list[dict],
    output_dir: Path,
    *,
    num_aspects: int = 3,
    max_examples: int | None = None,
    checkpoint_every: int = 10,
    batch_size: int = 32,
) -> tuple[Path, dict]:
    """Run DnA-Eval on pointwise benchmark items using vLLM."""
    if max_examples is not None:
        items = items[:max_examples]

    output_file = output_dir / f"judge_{judge_name}.json"
    aspects_file = output_dir / f"aspects_{judge_name}.json"
    weights_file = output_dir / f"weights_{judge_name}.json"

    results = load_checkpoint(output_file)
    aspects_cache = load_cache(aspects_file)
    weights_cache = load_cache(weights_file)
    start_idx = len(results)

    if start_idx >= len(items):
        logger.info("[%s] Already complete (%d results)", judge_name, start_idx)
        return output_file, compute_pointwise_metrics(results)

    logger.info(
        "[%s] Scoring items %d -> %d (vLLM, batch_size=%d)",
        judge_name, start_idx, len(items), batch_size,
    )

    # Pre-generate aspects and weights for all unique instructions (batched)
    remaining_items = items[start_idx:]
    unique_instructions = list(dict.fromkeys(it["prompt"] for it in remaining_items))
    uncached_instructions = [inst for inst in unique_instructions if inst not in aspects_cache]

    if uncached_instructions:
        logger.info("Generating aspects for %d unique instructions...", len(uncached_instructions))
        aspect_prompts = [
            (ASPECT_GEN_SYSTEM, ASPECT_GEN_USER.format(num_aspects=num_aspects, instruction=inst))
            for inst in uncached_instructions
        ]
        aspect_outputs = vllm_judge.generate_batch(aspect_prompts, batch_size=batch_size)
        for inst, raw in zip(uncached_instructions, aspect_outputs):
            if raw:
                aspects = parse_aspects(raw, num_aspects)
                if not aspects:
                    aspects = [line.strip() for line in raw.strip().split("\n") if line.strip()][:num_aspects]
                aspects_cache[inst] = aspects
        save_cache(aspects_file, aspects_cache)

    uncached_weights = [inst for inst in unique_instructions if inst not in weights_cache and inst in aspects_cache]
    if uncached_weights:
        logger.info("Generating weights for %d unique instructions...", len(uncached_weights))
        weight_prompts = []
        for inst in uncached_weights:
            aspects = aspects_cache[inst]
            aspects_text = "\n".join(f"{i+1}. {a}" for i, a in enumerate(aspects))
            weight_prompts.append((
                WEIGHT_GEN_SYSTEM,
                WEIGHT_GEN_USER.format(num_aspects=len(aspects), instruction=inst, aspects=aspects_text),
            ))
        weight_outputs = vllm_judge.generate_batch(weight_prompts, batch_size=batch_size)
        for inst, raw in zip(uncached_weights, weight_outputs):
            if raw:
                weights = parse_weights(raw, len(aspects_cache.get(inst, [])))
                if weights is None:
                    n = len(aspects_cache.get(inst, []))
                    weights = [1.0 / n] * n if n > 0 else [1.0]
                weights_cache[inst] = weights
        save_cache(weights_file, weights_cache)

    # Batch the per-aspect scoring calls
    for batch_start in tqdm(
        range(0, len(remaining_items), batch_size),
        desc=judge_name,
        total=(len(remaining_items) + batch_size - 1) // batch_size,
    ):
        batch = remaining_items[batch_start:batch_start + batch_size]

        scoring_prompts = []
        prompt_map = []
        for j, item in enumerate(batch):
            instruction = item["prompt"]
            aspects = aspects_cache.get(instruction, [])
            for a_idx, aspect in enumerate(aspects):
                user_msg = EVAL_POINTWISE_USER.format(
                    instruction=instruction,
                    response=item["response"],
                    aspect=aspect,
                )
                scoring_prompts.append((EVAL_POINTWISE_SYSTEM, user_msg))
                prompt_map.append((j, a_idx))

        scoring_outputs = vllm_judge.generate_batch(scoring_prompts, batch_size=batch_size * num_aspects)

        item_aspect_outputs = {j: [None] * num_aspects for j in range(len(batch))}
        for (j, a_idx), raw in zip(prompt_map, scoring_outputs):
            item_aspect_outputs[j][a_idx] = raw or ""

        for j, item in enumerate(batch):
            instruction = item["prompt"]
            aspects = aspects_cache.get(instruction, [])
            weights = weights_cache.get(instruction, [1.0 / max(len(aspects), 1)] * max(len(aspects), 1))

            aspect_scores = []
            raw_aspect_outputs_list = item_aspect_outputs[j]
            for raw in raw_aspect_outputs_list:
                score = parse_pointwise_score(raw)
                aspect_scores.append(score)

            all_valid = all(s is not None for s in aspect_scores)
            if all_valid:
                judge_score = aggregate_pointwise(weights, aspect_scores)
            else:
                judge_score = None

            result_entry = {
                "prompt": item["prompt"],
                "response": item["response"],
                "aspects": aspects,
                "weights": weights,
                "aspect_scores": aspect_scores,
                "raw_aspect_outputs": raw_aspect_outputs_list,
                "judge_score": judge_score,
                "human_score": item["human_score"],
            }
            for key in ["task_id", "domain", "response_model"]:
                if key in item:
                    result_entry[key] = item[key]
            results.append(result_entry)

        if len(results) % checkpoint_every == 0 or batch_start + batch_size >= len(remaining_items):
            save_checkpoint(output_file, results)

    save_cache(aspects_file, aspects_cache)
    save_cache(weights_file, weights_cache)
    metrics = compute_pointwise_metrics(results)
    logger.info("[%s] Done -- %d results -> %s", judge_name, len(results), output_file)
    return output_file, metrics


