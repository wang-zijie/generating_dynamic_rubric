def run_pairwise_judge(
    client: BedrockClient,
    judge_name: str,
    judge_config: dict,
    pairs: list[dict],
    output_dir: Path,
    *,
    num_aspects: int = 3,
    max_examples: int | None = None,
    checkpoint_every: int = 10,
) -> tuple[Path, dict]:
    """Run DnA-Eval on pairwise benchmark pairs.

    Per item: 2 calls (aspects+weights, cached per instruction) + 3 scoring calls.
    """
    if max_examples is not None:
        pairs = pairs[:max_examples]

    model_id = judge_config["model_id"]
    max_tokens = judge_config["max_tokens"]
    temperature = judge_config["temperature"]

    output_file = output_dir / f"judge_{judge_name}.json"
    aspects_file = output_dir / f"aspects_{judge_name}.json"
    weights_file = output_dir / f"weights_{judge_name}.json"

    results = load_checkpoint(output_file)
    aspects_cache = load_cache(aspects_file)
    weights_cache = load_cache(weights_file)
    start_idx = len(results)

    if start_idx >= len(pairs):
        logger.info(
            "[%s] Already complete (%d results)", judge_name, start_idx,
        )
        return output_file, compute_pairwise_metrics(results)

    logger.info(
        "[%s] Evaluating pairs %d -> %d (model=%s)",
        judge_name, start_idx, len(pairs), model_id,
    )

    for i, pair in enumerate(
        tqdm(
            pairs[start_idx:],
            desc=judge_name,
            initial=start_idx,
            total=len(pairs),
        ),
        start=start_idx,
    ):
        instruction = pair["instruction"]

        # Step 1: Generate aspects (cached per instruction)
        aspects = generate_aspects(
            client, model_id, instruction, aspects_cache,
            num_aspects=num_aspects,
            max_tokens=max_tokens,
            temperature=temperature,
        )

        # Step 2: Generate weights (cached per instruction)
        weights = generate_weights(
            client, model_id, instruction, aspects, weights_cache,
            max_tokens=max_tokens,
            temperature=temperature,
        )

        # Step 3: Per-aspect pairwise scoring (3 calls)
        aspect_scores_1 = []
        aspect_scores_2 = []
        raw_aspect_outputs = []

        for aspect in aspects:
            user_msg = EVAL_PAIRWISE_USER.format(
                instruction=instruction,
                output_1=pair["output_1"],
                output_2=pair["output_2"],
                aspect=aspect,
            )

            raw = call_judge(
                client,
                model_id,
                system=EVAL_PAIRWISE_SYSTEM,
                user_message=user_msg,
                max_tokens=64,
                temperature=temperature,
            )
            raw_aspect_outputs.append(raw)

            s1, s2 = parse_pairwise_scores(raw)
            aspect_scores_1.append(s1)
            aspect_scores_2.append(s2)

        # Step 4: Aggregation
        # Check for parse failures
        all_valid = all(
            s is not None for s in aspect_scores_1 + aspect_scores_2
        )

        if all_valid:
            ws1, ws2, preference = aggregate_pairwise(
                weights, aspect_scores_1, aspect_scores_2,
            )
        else:
            ws1, ws2, preference = None, None, None

        result_entry = {
            "instruction": instruction,
            "aspects": aspects,
            "weights": weights,
            "aspect_scores_1": aspect_scores_1,
            "aspect_scores_2": aspect_scores_2,
            "raw_aspect_outputs": raw_aspect_outputs,
            "weighted_score_1": ws1,
            "weighted_score_2": ws2,
            "preference": preference,
            "human_majority": pair["human_majority"],
        }
        for key in ["generator", "human_preferences"]:
            if key in pair:
                result_entry[key] = pair[key]

        results.append(result_entry)

        # Periodically save caches and checkpoint
        if (i + 1) % checkpoint_every == 0:
            save_checkpoint(output_file, results)
            save_cache(aspects_file, aspects_cache)
            save_cache(weights_file, weights_cache)
            interim = compute_pairwise_metrics(results)
            logger.info(
                "[%s] Checkpoint %d -- agreement=%.1f%%  errors=%d",
                judge_name, i + 1,
                interim["human_agreement_pct"],
                interim["n_parse_errors"],
            )

    # Final save
    save_checkpoint(output_file, results)
    save_cache(aspects_file, aspects_cache)
    save_cache(weights_file, weights_cache)
    metrics = compute_pairwise_metrics(results)
    logger.info(
        "[%s] Done -- %d results -> %s",
        judge_name, len(results), output_file,
    )
    return output_file, metrics


