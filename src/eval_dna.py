# ---------------------------------------------------------------------------
# Parsing helpers
# ---------------------------------------------------------------------------

def parse_aspects(raw: str, num_aspects: int = 3) -> list[str]:
    """Parse aspect questions from LLM output.

    Strips numbering (e.g., "1. ...", "Q1: ...", "(1) ...") and collects
    up to num_aspects questions.
    """
    questions = []
    for line in raw.strip().split("\n"):
        line = line.strip()
        if not line:
            continue
        # Strip common numbering patterns
        cleaned = re.sub(
            r"^\s*(?:\d+[\.\):]|Q\d+[\.\):]|\(\d+\))\s*", "", line,
        ).strip()
        if cleaned:
            questions.append(cleaned)
        if len(questions) >= num_aspects:
            break
    return questions


def parse_pairwise_scores(raw: str) -> tuple[float | None, float | None]:
    """Parse two space-separated scores from LLM output.

    Returns (score_1, score_2), each clamped to [1, 10], or None on failure.
    """
    m = re.search(r"(\d+(?:\.\d+)?)\s+(\d+(?:\.\d+)?)", raw.strip())
    if m:
        s1 = max(1.0, min(10.0, float(m.group(1))))
        s2 = max(1.0, min(10.0, float(m.group(2))))
        return s1, s2
    return None, None


def parse_pointwise_score(raw: str) -> float | None:
    """Parse a single score from LLM output.

    Returns score clamped to [1, 10], or None on failure.
    """
    m = re.search(r"(\d+(?:\.\d+)?)", raw.strip())
    if m:
        return max(1.0, min(10.0, float(m.group(1))))
    return None


def parse_weights(raw: str, num_aspects: int = 3) -> list[float] | None:
    """Parse percentage weights from LLM output.

    Extracts up to num_aspects percentage values and normalizes to sum to 1.0.
    Returns None on failure.
    """
    matches = re.findall(r"(\d+(?:\.\d+)?)\s*%?", raw.strip())
    if len(matches) < num_aspects:
        return None

    weights = [float(m) for m in matches[:num_aspects]]
    total = sum(weights)
    if total <= 0:
        return None

    return [w / total for w in weights]


# ---------------------------------------------------------------------------
# Aspect generation (with per-instruction caching)
# ---------------------------------------------------------------------------

def generate_aspects(
    client: BedrockClient,
    model_id: str,
    instruction: str,
    aspects_cache: dict,
    *,
    num_aspects: int = 3,
    max_tokens: int = 512,
    temperature: float = 0.0,
) -> list[str]:
    """Generate evaluation aspect questions for an instruction.

    Checks the in-memory cache first. Returns a list of aspect question strings.
    """
    if instruction in aspects_cache:
        return aspects_cache[instruction]

    user_msg = ASPECT_GEN_USER.format(
        num_aspects=num_aspects,
        instruction=instruction,
    )

    raw = call_judge(
        client,
        model_id,
        system=ASPECT_GEN_SYSTEM,
        user_message=user_msg,
        max_tokens=max_tokens,
        temperature=temperature,
    )

    aspects = parse_aspects(raw, num_aspects)

    if not aspects:
        logger.warning(
            "Failed to parse aspects from output: %s", raw[:200],
        )
        # Fallback: use each non-empty line as an aspect
        aspects = [
            line.strip() for line in raw.strip().split("\n")
            if line.strip()
        ][:num_aspects]

    aspects_cache[instruction] = aspects
    return aspects


# ---------------------------------------------------------------------------
# Weight generation (with per-instruction caching)
# ---------------------------------------------------------------------------

def generate_weights(
    client: BedrockClient,
    model_id: str,
    instruction: str,
    aspects: list[str],
    weights_cache: dict,
    *,
    max_tokens: int = 256,
    temperature: float = 0.0,
) -> list[float]:
    """Generate importance weights for evaluation aspects.

    Checks the in-memory cache first. Returns a list of weights summing to 1.0.
    """
    if instruction in weights_cache:
        return weights_cache[instruction]

    num_aspects = len(aspects)
    aspects_text = "\n".join(
        f"{i+1}. {a}" for i, a in enumerate(aspects)
    )

    user_msg = WEIGHT_GEN_USER.format(
        num_aspects=num_aspects,
        instruction=instruction,
        aspects=aspects_text,
    )

    raw = call_judge(
        client,
        model_id,
        system=WEIGHT_GEN_SYSTEM,
        user_message=user_msg,
        max_tokens=max_tokens,
        temperature=temperature,
    )

    weights = parse_weights(raw, num_aspects)

    if weights is None:
        logger.warning(
            "Failed to parse weights, using uniform. Raw: %s", raw[:200],
        )
        weights = [1.0 / num_aspects] * num_aspects

    weights_cache[instruction] = weights
    return weights


# ---------------------------------------------------------------------------
# Aggregation
# ---------------------------------------------------------------------------

def aggregate_pairwise(
    weights: list[float],
    scores_1: list[float],
    scores_2: list[float],
) -> tuple[float, float, int | None]:
    """Compute weighted scores and preference for pairwise evaluation.

    Returns (weighted_score_1, weighted_score_2, preference).
    preference is 1, 2, or None (tie).
    """
    ws1 = sum(w * s for w, s in zip(weights, scores_1))
    ws2 = sum(w * s for w, s in zip(weights, scores_2))

    if ws1 > ws2:
        preference = 1
    elif ws2 > ws1:
        preference = 2
    else:
        preference = None

    return ws1, ws2, preference


def aggregate_pointwise(
    weights: list[float],
    scores: list[float],
) -> float:
    """Compute weighted score for pointwise evaluation.

    Returns the weighted sum (on the 1-10 scale).
    """
    return sum(w * s for w, s in zip(weights, scores))


